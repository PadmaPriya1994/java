public class AreaGreater
{
public static void main(String args[])
{
int a=2;
int areaSquare=a*a;
System.out.println("The area of Square is "+areaSquare);
double r=1.5;
double areaCircle=3.14*r*r;
System.out.println("The area of Circle is "+areaCircle);
if(areaSquare>areaCircle)
{
System.out.println("Area of Square is greater and its value is "+areaSquare);
}
else
{
System.out.println("Area of Circle is greater and its value is "+areaCircle);
}
}
}
